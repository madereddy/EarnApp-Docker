#!/usr/bin/env bash
set -euo pipefail

# --------------------------
# Config — sourced from Docker ENV, with safe fallbacks
# --------------------------
APP_DIR="${APP_DIR:-/app/src/}"
BIN_PATH="${BIN_PATH:-/usr/bin/earnapp}"
CONFIG_DIR="${CONFIG_DIR:-/etc/earnapp}"
INSTALLER_URL="${INSTALLER_URL:-https://brightdata.com/static/earnapp/install.sh}"
CDN_BASE="${CDN_BASE:-https://cdn-earnapp.b-cdn.net/static}"
EARNAPP_VERSION="${EARNAPP_VERSION:-}"#!/usr/bin/env bash
set -euo pipefail

APP_DIR="${APP_DIR:-/app/src/}"
BIN_PATH="${BIN_PATH:-/usr/bin/earnapp}"
CONFIG_DIR="${CONFIG_DIR:-/etc/earnapp}"
INSTALLER_URL="${INSTALLER_URL:-https://brightdata.com/static/earnapp/install.sh}"
CDN_BASE="${CDN_BASE:-https://cdn-earnapp.b-cdn.net/static}"
DEBUG_MODE="${DEBUG_MODE:-0}"

# --------------------------
# Debug mode
# --------------------------
if [[ "${DEBUG_MODE:-}" == "1" ]]; then
    echo "[INFO] DEBUG_MODE enabled, launching shell..."
    exec bash
fi

# --------------------------
# Ensure directories exist
# --------------------------
mkdir -p "$APP_DIR" "$CONFIG_DIR"
touch "$CONFIG_DIR/status"

# --------------------------
# Install EarnApp at runtime via patched installer
# --------------------------
if [[ ! -x "$BIN_PATH" ]]; then
    echo "[INFO] EarnApp binary not found, installing..."

    TMP_INSTALL="/tmp/earnapp.sh"
    curl -fsSL "$INSTALLER_URL" -o "$TMP_INSTALL"

    # Patch install.sh to set version if EARNAPP_VERSION is defined
    if [[ -n "${EARNAPP_VERSION:-}" ]]; then
        echo "[INFO] Patching installer to install version $EARNAPP_VERSION..."
        sed -i "s/^VERSION=\"[^\"]*\"/VERSION=\"$EARNAPP_VERSION\"/" "$TMP_INSTALL"
    fi

    if [[ "$DEBUG_MODE" == "1" ]]; then
        echo "yes" | bash "$TMP_INSTALL"
    else
        echo "yes" | bash "$TMP_INSTALL" &>/dev/null
    fi
    rm -f "$TMP_INSTALL"
    echo "[INFO] EarnApp installed successfully."
fi

# --------------------------
# UUID handling
# --------------------------
if [[ -n "${EARNAPP_UUID:-}" ]]; then
    echo -n "$EARNAPP_UUID" > "$CONFIG_DIR/uuid"
    echo "[INFO] Using UUID from environment"
elif [[ ! -f "$CONFIG_DIR/uuid" ]]; then
    echo "[WARN] No UUID found — will be generated by EarnApp"
fi
chmod 600 "$CONFIG_DIR/"*

# --------------------------
# Start EarnApp to generate UUID
# --------------------------
echo "[INFO] Starting EarnApp..."
"$BIN_PATH" stop 2>/dev/null || true
sleep 1
"$BIN_PATH" start 2>/dev/null || true
sleep 2

# --------------------------
# Wait for UUID registration
# --------------------------
DEVICE_ID="unknown"
MAX_ATTEMPTS=5
BACKOFF=2
for i in $(seq 1 $MAX_ATTEMPTS); do
    DEVICE_ID=$(("$BIN_PATH" showid 2>/dev/null || true) | tr -d '[:space:]')
    if [[ -n "$DEVICE_ID" && "$DEVICE_ID" != "undefined" ]]; then break; fi
    echo "[INFO] Waiting for EarnApp registration (attempt $i/$MAX_ATTEMPTS)..."
    "$BIN_PATH" stop 2>/dev/null || true
    sleep 1
    "$BIN_PATH" start 2>/dev/null || true
    sleep "$BACKOFF"
    BACKOFF=$((BACKOFF * 2))
done
DEVICE_ID=${DEVICE_ID:-$(cat "$CONFIG_DIR/uuid" 2>/dev/null || echo "unknown")}

# --------------------------
# Print registration info
# --------------------------
echo "------------------------------------------------------------"
if [[ "$DEVICE_ID" == "unknown" ]]; then
    echo "[WARN] UUID not yet available; registration may still be in progress."
else
    echo "[INFO] Device ID: $DEVICE_ID"
    echo "[INFO] Visit: https://earnapp.com/r/$DEVICE_ID to register"
fi
echo "------------------------------------------------------------"

# --------------------------
# Run EarnApp loop
# --------------------------
BACKOFF=5
MAX_BACKOFF=300
while true; do
    START_TIME=$(date +%s)
    "$BIN_PATH" run || true
    RUN_DURATION=$(( $(date +%s) - START_TIME ))

    if [[ $RUN_DURATION -gt 60 ]]; then
        BACKOFF=5
        echo "[INFO] EarnApp exited after ${RUN_DURATION}s, restarting in ${BACKOFF}s..."
    else
        echo "[WARN] EarnApp crashed after ${RUN_DURATION}s, backing off ${BACKOFF}s..."
        sleep "$BACKOFF"
        BACKOFF=$((BACKOFF * 2))
        [[ $BACKOFF -gt $MAX_BACKOFF ]] && BACKOFF=$MAX_BACKOFF
    fi

    "$BIN_PATH" stop 2>/dev/null || true
    sleep 1
    "$BIN_PATH" start 2>/dev/null || true
    sleep 2
done
